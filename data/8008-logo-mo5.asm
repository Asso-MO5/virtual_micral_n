    LEL

    LAB

    ; Extract Char in A
    LBI 0xC0
    NDB
    RLC
    RLC

    LHI \HB\CHAR
    LLI \LB\CHAR
    ADL
    LLA ; Works only if L doesn't overflow
    LAM

    LHD
    LLE

OUTPUT:
    CAL OUTPUT_CHAR
    DCC
    JFZ OUTPUT

    CAL ADVANCE_HL
    JMP LOOP

STR:
    CAL ADVANCE_HL

OUTPUT_STR:
    LAM
    ORA
    JTZ END
    CAL OUTPUT_CHAR
    CAL ADVANCE_HL
    JMP OUTPUT_STR

END:
    HLT

    ; Output of A content to the Serial Card
    ; Uses B
OUTPUT_CHAR:
    LBA

W_OUTPUT_R:
    ; Wait for Output Ready
    LAI 0x01
    INP 0x0
    RLC
    JFC W_OUTPUT_R

    LAB
    OUT 0x0
    RET

ADVANCE_HL:
    INL
    RFZ
    INH
    RET

